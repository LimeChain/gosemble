var remarkCall = types.Call{
	CallIndex: types.CallIndex{
		ModuleIndex:   system.Module.Index,
		FunctionIndex: system.Module.Functions["remark"].Index,
	},
	Args: sc.Sequence[sc.U8]{0xab, 0xcd},
}

func Test_EncodeUncheckedExtrinsic_Unsigned(t *testing.T) {
	var testExamples = []struct {
		label       string
		input       types.UncheckedExtrinsic
		expectation []byte
	}{
		{
			label:       "Encode(UnsignedUncheckedExtrinsic)",
			input:       types.NewUnsignedUncheckedExtrinsic(remarkCall),
			expectation: []byte{0x18, 0x4, 0x0, 0x0, 0x8, 0xab, 0xcd},
		},
	}

	for _, testExample := range testExamples {
		t.Run(testExample.label, func(t *testing.T) {
			buffer := &bytes.Buffer{}

			testExample.input.Encode(buffer)

			require.Equal(t, testExample.expectation, buffer.Bytes())
			require.Equal(t, sc.Bool(false), testExample.input.IsSigned())
		})
	}
}

